final case class AesPrg(key: BitVecN[128]) extends Prg[BitVecN[128], BitVecN[128]] {

  private val roundKeys = AesPrg.expandKey(key.raw.bytes, 10)

  override def apply(seed: BitVecN[128], numBlocks: Int): Vector[BitVecN[128]] = {
    val out = Vector.newBuilder[BitVecN[128]]
    out.sizeHint(numBlocks)

    var pos = UBitInt[128](0)
    while out.knownSize < numBlocks do {
      var result = seed.xor(pos.toBitVecN).raw.bytes
      pos = pos + UBitInt[128](1)

      var j = 1
      while j < 10 do {
        result = AesPrg.mmAesencSi128(result, roundKeys(j))
        j += 1
      }

      result = AesPrg.mmAesenclastSi128(result, roundKeys(10))
      result = result.xor(seed.raw.bytes)

      out.addOne(BitVecN(result.bits))
    }

    out.result()
  }

  object Seeding extends tf.bug.dpf.Seeding[BitVecN[128]] {
    override val seedIsCorrectable: Correctable[BitVecN[128]] = summon

    override def expand(root: BitVecN[128]): (BitVecN[128], BitVecN[128]) = {
      val key0 = roundKeys(0)
      val key1 = new Uint8Array(key0)
      key1(15) = (key1(15) ^ 1).toShort

      var result0 = root.raw.bytes.xor(ByteVector.fromUint8Array(key0))
      var result1 = root.raw.bytes.xor(ByteVector.fromUint8Array(key1))

      var j = 1
      while j < 10 do {
        result0 = AesPrg.mmAesencSi128(result0, roundKeys(j))
        result1 = AesPrg.mmAesencSi128(result1, roundKeys(j))
        j += 1
      }

      result0 = AesPrg.mmAesenclastSi128(result0, roundKeys(10))
      result1 = AesPrg.mmAesenclastSi128(result1, roundKeys(10))

      result0 = result0.xor(root.raw.bytes)
      result1 = result1.xor(root.raw.bytes)

      (BitVecN(result0.bits), BitVecN(result1.bits))
    }
  }

}

