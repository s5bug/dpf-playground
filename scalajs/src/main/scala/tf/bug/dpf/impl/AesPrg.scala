  private def addRoundKey(state: Uint8Array, roundKey: Uint8Array): Unit =
    (0 until 16).foreach { i =>
      state(i) = (state(i) ^ roundKey(i)).toShort
    }

  private def subBytes(state: Uint8Array): Unit =
    (0 until 16).foreach { i =>
      state(i) = sBox(state(i))
    }

  private def shiftRows(state: Uint8Array): Unit =
    state.set(Uint8Array.of(
      state(0), state(5), state(10), state(15),
      state(4), state(9), state(14), state(3),
      state(8), state(13), state(2), state(7),
      state(12), state(1), state(6), state(11)
    ))

  private def gMul(a: Short, b: Short): Short = {
    if a == 0 || b == 0 then 0
    else if a == 1 then b
    else if b == 1 then a
    else e((l(a) + l(b)) % 0xFF)
  }

  private def mixColumns(state: Uint8Array, matrix: Array[Uint8Array]): Unit = {
    var c = 0
    while c < 4 do {
      val offset = c * 4
      val s0 = state(offset)
      val s1 = state(offset + 1)
      val s2 = state(offset + 2)
      val s3 = state(offset + 3)

      state(offset) = (gMul(2, s0) ^ gMul(3, s1) ^ s2 ^ s3).toShort
      state(offset + 1) = (s0 ^ gMul(2, s1) ^ gMul(3, s2) ^ s3).toShort
      state(offset + 2) = (s0 ^ s1 ^ gMul(2, s2) ^ gMul(3, s3)).toShort
      state(offset + 3) = (gMul(3, s0) ^ s1 ^ s2 ^ gMul(2, s3)).toShort

      c += 1
    }
  }

  private def mmAesencSi128(state: ByteVector, roundKey: Uint8Array): ByteVector = {
    val in = state.toUint8Array
    subBytes(in)
    shiftRows(in)
    mixColumns(in, mixColumnsMatrix)
    addRoundKey(in, roundKey)
    ByteVector.fromUint8Array(in)
  }

  private def mmAesenclastSi128(state: ByteVector, roundKey: Uint8Array): ByteVector = {
    val in = state.toUint8Array
    subBytes(in)
    shiftRows(in)
    addRoundKey(in, roundKey)
    ByteVector.fromUint8Array(in)
  }

  private def rotWord(bv: ByteVector): ByteVector =
    bv.drop(1) :+ bv.head

  private def expandKey(key: ByteVector, rounds: Int): Array[Uint8Array] = {
    val numWords = (1 + rounds) * 4
    val expanded = new Uint8Array(numWords * 4)
    expanded.set(key.toUint8Array)

    var i = 4
    val temp = new Uint8Array(4)
    while i < numWords do {
      temp(0) = expanded((i - 1) * 4 + 0)
      temp(1) = expanded((i - 1) * 4 + 1)
      temp(2) = expanded((i - 1) * 4 + 2)
      temp(3) = expanded((i - 1) * 4 + 3)

      if i % 4 == 0 then {
        val t = temp(0)
        temp(0) = temp(1)
        temp(1) = temp(2)
        temp(2) = temp(3)
        temp(3) = t

        temp(0) = sBox(temp(0))
        temp(1) = sBox(temp(1))
        temp(2) = sBox(temp(2))
        temp(3) = sBox(temp(3))

        temp(0) = (temp(0) ^ rCon((i / 4) - 1)).toShort
      }

      expanded(i * 4 + 0) = (expanded((i - 4) * 4 + 0) ^ temp(0)).toShort
      expanded(i * 4 + 1) = (expanded((i - 4) * 4 + 1) ^ temp(1)).toShort
      expanded(i * 4 + 2) = (expanded((i - 4) * 4 + 2) ^ temp(2)).toShort
      expanded(i * 4 + 3) = (expanded((i - 4) * 4 + 3) ^ temp(3)).toShort

      i += 1
    }

    val b = new Array[Uint8Array](1 + rounds)
    var r = 0
    while r <= rounds do {
      b(r) = expanded.subarray(16 * r, 16 * (1 + r))
      r += 1
    }
    b
  }

